export async function POST(request: NextRequest) {
  try {
    const body = await request.json().catch(() => ({}));
    const targetDomain = body.domain || 'medical';
    const targetSource = body.source || 'custom';

    // Support custom entries from request body
    const customEntries = Array.isArray(body.entries) ? body.entries : [];
    const allEntries = [...bulkKnowledge, ...customEntries];

    let added = 0;
    let skipped = 0;
    let errors = 0;

    for (const item of allEntries) {
      const fingerprint = md5(`${item.title}_${item.content}`);

      try {
        await db.knowledgeEntry.upsert({
          where: {
            domain_source_fingerprint: {
              domain: targetDomain,
              source: targetSource,
              fingerprint,
            },
          },
          update: {},
          create: {
            domain: targetDomain,
            source: targetSource,
            title: item.title,
            content: item.content,
            category: item.category,
            summary: item.summary || null,
            symptoms: item.symptoms || null,
            etiology: item.etiology || null,
            diagnosis: item.diagnosis || null,
            treatment: item.treatment || null,
            prevention: item.prevention || null,
            fingerprint,
          },
        });
        added++;
      } catch (e: unknown) {
        if (e && typeof e === 'object' && 'code' in e && (e as { code?: string }).code === 'P2002') {
          // Unique constraint violation - skip duplicate
          skipped++;
        } else {
          console.error(`[Bulk Seed] Error adding "${item.title}":`, e);
          errors++;
        }
      }
    }

    const result = {
      success: true,
      domain: targetDomain,
      source: targetSource,
      total_entries: allEntries.length,
      entries_added: added,
      entries_skipped: skipped,
      errors,
      message: `批量导入完成：新增 ${added} 条，跳过 ${skipped} 条重复，错误 ${errors} 条`,
    };

    console.log('[Bulk Seed]', result.message);

    return Response.json(result);
  } catch (error) {
    console.error('[Bulk Seed API] 导入失败:', error);
    return Response.json({ error: '批量种子数据导入失败' }, { status: 500 });
  }
}

export async function GET() {
  try {
    const customEntries = await db.knowledgeEntry.findMany({
      where: {
        domain: 'medical',
        source: 'custom',
      },
      select: {
        id: true,
        title: true,
        category: true,
        source: true,
        createdAt: true,
      },
      orderBy: { createdAt: 'desc' },
    });

    return Response.json({
      total: customEntries.length,
      entries: customEntries,
    });
  } catch (error) {
    console.error('[Bulk Seed API] 查询失败:', error);
    return Response.json({ error: '查询失败' }, { status: 500 });
  }
}
